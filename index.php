         <mark> <em><strong> Covid-19/ SARS-CoV-2:</strong></em> </mark> A finales de diciembre de 2019 se notificaron los primeros casos de un nuevo coronavirus en la ciudad de Wuhan (China). Desde entonces el aumento de nuevos infectados por el virus SARS-CoV-2 (inicialmente llamado 2019nCoV), que provoca la enfermedad denominada Covid-19, ha sido continuo y su transmisión de persona a persona se ha acelerado. Los casos declarados ya superan con creces a los de la epidemia de SARS de 2002-2003. Su tasa de letalidad es más baja que la de otros coronavirus, pero se están produciendo muchos más fallecimientos (se acercan al millón, según las cifras oficiales) porque las personas infectadas ya se cuentan por decenas de millones en todo el mundo.</p> 

<p> La Organización Mundial de la Salud (OMS) ha declarado la situación de pandemia. Hay personas infectadas en la mayoría de los países y los profesionales sanitarios insisten en la necesidad de seguir las medidas preventivas y de control.</p>

<p>En España y otros países se decretó el estado de alarma y, tras un periodo de confinamiento, se entró en la denominada nueva normalidad. Sin embargo, los casos han vuelto a aumentar y ya se habla de una segunda ola de la pandemia. </p>

